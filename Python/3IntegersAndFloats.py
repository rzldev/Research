## Create a variable with numeric value
num = 3

print(num)

## Check the class type of the variable
num1 = 10
num2 = 0.1

print(type(num1))
print(type(num2))

## Arithmetic operators
num1 = 3
num2 = 2

# Addition
print(num1 + num2)

# Substraction
print(num1 - num2)

# Multiplication
print(num1 * num2)

# Division
print(num1 / num2)

# Floor Division
print(num1 // num2)

# Exponent
print(num1 ** num2)

# Modulus
print(num1 % num2)

## Using Brackets
print(num1 + num1 * num2)
print((num1 + num1) * num2)

## Short some code
num = 1
num = num + 1

print(num)

number = 1
number += 1

print(number)

## Print an absolute numeric value
num = -5

print(abs(num))

## Round numeric value
phi = 3.14

print(round(phi))
print(round(phi, 1))
print(round(phi, 2))

## Comparisons
num1 = 3
num2 = 5

# Equal
print(num1 == num2)

# Not Equal
print(num1 != num2)

# Greater Than
print(num1 > num2)

# Less Than
print(num1 < num2)

# Greater or Equal
print(num1 >= num2)

# Less or Equal
print(num1 <= num2)

## Casting / Converting
num1 = '100'
num2 = '200'

print(num1 + num2)

num1 = int(num1)
num2 = int(num2)

print(num1 + num2)
